/* Problem 1: Sum of Two Numbers

Write a JavaScript function called addNumbers that takes two numbers as parameters and returns their sum. */

function addNumbers(number1, number2) {
  return number1 + number2;
}

console.log(addNumbers(2, 3));
console.log(addNumbers(107, 2));
console.log(addNumbers(47, 1));
console.log(addNumbers(12, 314));
console.log(addNumbers(1, 30));
